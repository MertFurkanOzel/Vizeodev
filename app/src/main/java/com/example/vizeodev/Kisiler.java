package com.example.vizeodev;

public class Kisiler
{
    private String isim;
    private String numara;
    private int foto;

    public int get_foto() {
        return foto;
    }

    public void set_foto(int foto) {
        this.foto = foto;
    }

    public String get_isim()
    {
        return isim;
    }

    public void set_isim(String isim)
    {
        this.isim = isim;
    }

    public String get_numara()
    {
        return numara;
    }

    public void set_numara(String numara)
    {
        this.numara = numara;
    }
}
